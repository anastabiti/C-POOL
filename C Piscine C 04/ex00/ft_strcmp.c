int	ft_strcmp(char *s1, char *s2)
{
	int	anas;

	anas = 0;
	while (s1[anas] != 0 && s2[anas] != 0 && s1[anas] == s2[anas])
	{
		anas++;
	}
	return (s1[anas] - s2[anas]);
}
